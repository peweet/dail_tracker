# ── State helpers ──────────────────────────────────────────────────────────────

def _init() -> None:
    for k, v in {
        "lob_selected_politician": None,
        "lob_selected_org":        None,
        "lob_selected_area":       None,
        "lob_selected_revdoor":    False,
        "lob_selected_dpo":        None,
        "lob_sidebar_search":      "",
        "lob_date_start":          None,
        "lob_date_end":            None,
    }.items():
        st.session_state.setdefault(k, v)


def _clear_profile() -> None:
    st.session_state.lob_selected_politician = None
    st.session_state.lob_selected_org        = None
    st.session_state.lob_selected_area       = None
    st.session_state.lob_selected_revdoor    = False
    st.session_state.lob_selected_dpo        = None


def _nav_pol(name: str) -> None:
    _clear_profile()
    st.session_state.lob_selected_politician = name


def _nav_org(name: str) -> None:
    _clear_profile()
    st.session_state.lob_selected_org = name


def _nav_area(area: str) -> None:
    _clear_profile()
    st.session_state.lob_selected_area = area


def _nav_revdoor() -> None:
    _clear_profile()
    st.session_state.lob_selected_revdoor = True


def _nav_dpo(name: str) -> None:
    st.session_state.lob_selected_dpo = name


