def _apply_year_filter(df: pd.DataFrame, key: str) -> pd.DataFrame:
    """Render a year radio above a detail table and return the filtered DataFrame."""
    if df.empty or "period_start_date" not in df.columns:
        return df
    try:
        years = sorted(
            df["period_start_date"].dropna().apply(
                lambda x: x.year if hasattr(x, "year") else int(str(x)[:4])
            ).unique(),
            reverse=True,
        )
    except Exception:
        return df
    if not years:
        return df
    options = ["All"] + [str(y) for y in years]
    chosen = st.radio("Year", options, horizontal=True, key=key, label_visibility="collapsed")
    if chosen != "All":
        df = df[df["period_start_date"].apply(
            lambda x: (x.year if hasattr(x, "year") else int(str(x)[:4])) == int(chosen)
        )]
    return df


def _provenance_footer(summary: pd.DataFrame | pd.Series) -> None:
    with st.expander("About & data provenance", expanded=False):
        st.markdown(
            "**Data source:** lobbying.ie — the Irish Register of Lobbying. "
            "Returns are filed quarterly by organisations lobbying Designated Public Officials (DPOs). "
            "Data is extracted via the lobbying.ie API and processed by the Dáil Tracker pipeline."
        )
        st.divider()
        if not isinstance(summary, pd.Series):
            summary = summary.iloc[0] if not summary.empty else pd.Series()
        src = summary.get("source_summary", "lobbying.ie via lobby_processing.py")
        ts  = summary.get("latest_fetch_timestamp_utc", "—")
        fp  = summary.get("first_period", "—")
        lp  = summary.get("last_period",  "—")
        st.caption(f"Source: {src}")
        st.caption(f"Dataset covers: {fp} → {lp}  ·  Last fetched: {ts}")


# ── Sidebar ────────────────────────────────────────────────────────────────────

def _render_sidebar() -> None:
    with st.sidebar:
        st.markdown('<p class="lob-sidebar-label">Search</p>', unsafe_allow_html=True)
        search = st.text_input(
            "Search",
            placeholder="e.g. Ibec, Mary Lou McDonald",
            key="lob_sidebar_search",
            label_visibility="collapsed",
        )

        pol_names = fetch_all_politician_names()
        org_names = fetch_all_org_names()

        s = search.strip().lower()
        if s:
            pol_filtered = [n for n in pol_names if s in n.lower()]
            org_filtered = [n for n in org_names if s in n.lower()]
        else:
            pol_filtered = pol_names[:200]
            org_filtered = []

        combined_labels = (
            [""] +
            pol_filtered +
            [f"[Org] {n}" for n in org_filtered[:50]]
        )

        sel = st.selectbox(
            "Browse all members",
            combined_labels,
            label_visibility="collapsed",
        )
        if sel:
            if sel.startswith("[Org] "):
                _nav_org(sel[6:])
            else:
                _nav_pol(sel)
            st.rerun()

        st.divider()

        st.markdown('<p class="lob-sidebar-label">Notable targets</p>', unsafe_allow_html=True)
        notable = ["An Taoiseach", "Minister for Finance", "Tánaiste", "Minister for Health"]
        chip_cols = st.columns(2)
        for i, chip in enumerate(notable):
            if chip_cols[i % 2].button(chip, key=f"lob_chip_{i}", use_container_width=True):
                idx = fetch_politician_index()
                if not idx.empty and "position" in idx.columns:
                    m = idx[idx["position"].str.contains(chip, case=False, na=False)]
                    if not m.empty:
                        _nav_pol(m.iloc[0]["member_name"])
                        st.rerun()
                    else:
                        st.caption(f"No politician found with position matching '{chip}'.")

        areas = fetch_policy_area_summary()
        if not areas.empty and "public_policy_area" in areas.columns:
            st.divider()
            st.markdown('<p class="lob-sidebar-label">Browse by policy area</p>', unsafe_allow_html=True)
            top_areas = areas["public_policy_area"].dropna().head(10).tolist()
            for area in top_areas:
                safe_key = f"lob_area_{area[:25].replace(' ', '_')}"
                if st.button(area, key=safe_key, use_container_width=True):
                    _nav_area(area)
                    st.rerun()


